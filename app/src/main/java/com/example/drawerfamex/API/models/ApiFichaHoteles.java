package com.example.drawerfamex.API.models;

public class ApiFichaHoteles {
    private int id;
    private String imagenEsp;
    private String imagenEn;
    private String imagenFr;
    private String linkES;
    private String linkEN;
    private String linkFR;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getImagenEsp() {
        return imagenEsp;
    }

    public void setImagenEsp(String imagenEsp) {
        this.imagenEsp = imagenEsp;
    }

    public String getImagenEn() {
        return imagenEn;
    }

    public void setImagenEn(String imagenEn) {
        this.imagenEn = imagenEn;
    }

    public String getImagenFr() {
        return imagenFr;
    }

    public void setImagenFr(String imagenFr) {
        this.imagenFr = imagenFr;
    }

    public String getLinkES() {
        return linkES;
    }

    public void setLinkES(String linkES) {
        this.linkES = linkES;
    }

    public String getLinkEN() {
        return linkEN;
    }

    public void setLinkEN(String linkEN) {
        this.linkEN = linkEN;
    }

    public String getLinkFR() {
        return linkFR;
    }

    public void setLinkFR(String linkFR) {
        this.linkFR = linkFR;
    }
}
